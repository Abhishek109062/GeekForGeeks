//User function Template for Java

class Solution
{
    // arr[]: Input Array
    // N : Size of the Array arr[]
    //Function to count inversions in the array.
    static long inversionCount(long arr[], long N)
    {
        long count=0L;
        for(int x=0;x<N;x++){
            for(int y=x+1;y<N;y++){
                if(arr[x]>arr[y])
                count++;
            }
        }
        return count;
    }
}
